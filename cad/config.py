def m5_rod_centers() -> list[tuple[float, float]]:
    """Return the four mk0.12 M5 through-rod centers in XY coordinates."""
    ox = M5_ROD_CENTER_OFFSET_X
    oy = M5_ROD_CENTER_OFFSET_Y
    return [(-ox, -oy), (ox, -oy), (-ox, oy), (ox, oy)]


def fan_screw_centers() -> list[tuple[float, float]]:
    """Return 120 mm fan screw centers for the standard 105 mm pattern."""
    offset = FAN_SCREW_CENTER_OFFSET
    return [(-offset, -offset), (offset, -offset), (-offset, offset), (offset, offset)]


def rect_bounds(center_x: float, center_y: float, width: float, depth: float) -> tuple[float, float, float, float]:
    """Return rectangle bounds as (x_min, x_max, y_min, y_max)."""
    half_width = width / 2
    half_depth = depth / 2
    return (
        center_x - half_width,
        center_x + half_width,
        center_y - half_depth,
        center_y + half_depth,
    )
